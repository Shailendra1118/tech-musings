    private static boolean isValidSudoku(String[][] board) {
        for(int i=0; i<9; i++) {
            for(int j=0; j<9; j++) {
                if(board[i][j] != ".") {
                    if(isValid(board, i, j, board[i][j]) == false)
                        return false;
                }
            }
        }

        return true;
    }

    private static boolean isValid(String[][] board, int row, int col, String num) {
        int blocki0 = (row/3) * 3;
        int blockj0 = (col/3) * 3;
        for(int i=0; i<9; i++){
            //check all columns of row
            int blockRow = blocki0+ i/3;
            int blockCol = blockj0+ i%3;
            if(i == col || i == row)
                continue;
            if(board[row][i] == num){
                return false;
            }
            //check all rows with this col
            if(board[i][col] == num){
                return false;
            }
            if(blockRow == row && blockCol == col)
                continue;
            if(board[blockRow][blockCol] == num){
                return false;
            }
        }
        return true;
    }
